void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){
	
	if (huart == &huart1){
		crc = 0;
		static uint32_t count = 0;
		for (int i = 1; i<8; i++){
			crc +=strUart[i];
		}
		crc = 0xFF - crc + 1;
		if (crc == strUart[8]){
			co2_ppm = strUart[2]*256+strUart[3];
		} else count++; 
		if (count == 30){
			count = 0;
			co2_ppm = 0;
		}
		HAL_UART_Receive_IT(&huart1,(uint8_t*) strUart,9);
	}
	if (huart == &huart2){
		HAL_UART_Receive_IT(&huart2,(uint8_t*) strAndroid,1);
	}		
}
